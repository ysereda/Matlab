%% Function
function [f , g , H] = rosenbrock (x)
    f = 100*(x(2) - x(1)^2)^2 + (1 - x(1))^2;
    g (1) = 400*x(1)*(x(1)^2 - x(2)) + 2*x(1) - 2;
    g (2) = 200*(x(2) - x(1)^2);
    H (1 ,1) = 1200*x(1)^2 - 400*x(2) + 2; % diff(f(x1,x2),x1,x1)
    H (1 ,2) = -400*x(1);
    H (2 ,1) = -400*x(1);
    H (2 ,2) = 200;
end